    void fillList() {
        lm.clear();
        allDevices = new ArrayList<DeviceDetails>();
        try {
            String ssql = "select * from devicedetails";
            Statement stmt = con.createStatement(ResultSet.CONCUR_UPDATABLE, ResultSet.TYPE_SCROLL_INSENSITIVE);
            ResultSet rs = stmt.executeQuery(ssql);
            while (rs.next()) {
                DeviceDetails singleDevice = new DeviceDetails();
                singleDevice.deviceID = rs.getInt(1);
                singleDevice.DeviceName = rs.getString(2);
                singleDevice.type = rs.getString(3);
                singleDevice.location = rs.getString(4);
                singleDevice.service = rs.getString(5);
                allDevices.add(singleDevice);
            }
        } catch (Exception e) {
            System.out.println("Error: " + e);
        }
        for (int i = 0; i < allDevices.size(); i++) {
            lm.addElement(allDevices.get(i).DeviceName + " - "+allDevices.get(i).service);
        }
        jList1.setModel(lm);
    }

    public void ImageTransform(int ww, int hh) {
        scSmall = new BufferedImage(ww, hh, BufferedImage.TYPE_INT_RGB);
        gDest = scSmall.createGraphics();
        gDest.setRenderingHint(RenderingHints.KEY_INTERPOLATION, RenderingHints.VALUE_INTERPOLATION_BICUBIC);
        //gDest.drawImage(sc,0, 0, fb.WW, fb.HH, null);
        AffineTransform at = AffineTransform.getScaleInstance((double) 330 / thumbIn.getWidth(), (double) 141 / thumbIn.getHeight());
        gDest.drawRenderedImage(thumbIn, at);
    }

